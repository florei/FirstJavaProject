public class Calculator {
    public int inmultire (int first, int second) {
        int result = first * second;
        return result;
    }

    public float modul (float first, float second) {
        float result = first % second;
        return result;
    }
    public float adunare (float first, float second) {
        float result = first + second;
        return result;
    }
    public int scadere (int first, int second) {
        int result = first - second;
        return result;
    }
    public float impartire (int first, float second) {
        float result = first * second;
        return result;
    }
}
